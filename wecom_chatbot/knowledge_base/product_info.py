def get_full_knowledge_text() -> str:
    """将所有产品知识拼装成一段文本，供 AI 引擎检索和参考。"""
    sections = [BRAND_INTRO, ""]

    for model_name, info in PRODUCT_MODELS.items():
        lines = [f"## {model_name}"]
        for key, value in info.items():
            if isinstance(value, list):
                lines.append(f"- {key}: " + "、".join(value))
            else:
                lines.append(f"- {key}: {value}")
        sections.append("\n".join(lines))

    sections.append("\n## 共通特点\n" + "\n".join(f"- {f}" for f in COMMON_FEATURES))

    sections.append("\n## FAQ")
    for item in FAQ:
        sections.append(f"Q: {item['问题']}\nA: {item['回答']}")

    return "\n\n".join(sections)
